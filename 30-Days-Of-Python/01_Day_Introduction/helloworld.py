print(3 + 2)   # addition(+) does the normal addition
print(3 - 2)   # subtraction(-) does the normal subtraction
print(3 * 2)   # multiplication(*) does the normal multiplication
print(3 / 2)   # division(/) will divide the number and give float number
print(3 ** 2)  # exponential(**) used as a power of
print(3 % 2)   # modulus(%) gives the answer of a division by sending only the remainder part
print(3 // 2)  # Floor division operator(//) it rerunds the integer part of the division

# Checking data types

print(type(10))                  # Int is a whole number 1,2,3,4,5,6
print(type(3.14))                # Float is a decimal point number 0.34343434343545
print(type(1 + 3j))              # Complex  is a number with real and imaginary parts like 2 + 3i
print(type('Asabeneh'))          # String is a sequence of characters
print(type([1, 2, 3]))           # List  is an orderd changeble collection of items index start at 0
print(type({'name':'Asabeneh',
            'age':30}))          # Dictionary  is a list but with a key value pair e.g name is the key and Asabeneh is the value  the key is always in string
print(type({9.8, 3.14, 2.7}))    # Set  is a collection which is unordered unindexed and unchangeable duplicates not allowed
print(type((9.8, 3.14, 2.7)))    # Tuple is a collection which is ordered and unchangeable duplicates allowed
print(type(3 == 3))              # Bool
print(type(3 > 3))              # Bool  it only has two outputs a true or a false
